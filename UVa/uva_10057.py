while 1:
    try:
        lst = []
        for _ in range(int(input())):
            lst.append(int(input()))
        lst.sort()

        mid1 = lst[(len(lst)-1)//2]
        mid2 = lst[len(lst)//2]
        count = 0
        for i in range(len(lst)):
            if lst[i]==mid1 or lst[i]==mid2:
                count += 1
        print(mid1, count, mid2-mid1+1)
    except:
        break
